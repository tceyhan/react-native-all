import {createSlice} from '@reduxjs/toolkit';

export const jobSlice = createSlice({
  name: 'job',
  initialState: {},
  reducers: {
    add: state => {
      state.value += 1;
    },
    remove: state => {
      state.value -= 1;
    },
  },
});

export const {add, remove} = jobSlice.actions;
export default jobSlice.reducer;
